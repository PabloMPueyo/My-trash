public class Multiplos13 {
    public static void main(String[] args) {
        int n;
        System.out.println("Multiplos de 2: ");
        for (n = 0; n <= 100; n++) {
            if (n % 2 == 0) {
                System.out.print(n + " ");
            }}
        System.out.println("");
        System.out.println("Multiplos de 3: ");
        for (n = 0; n <= 100; n++) {
            if (n % 3 == 0) {
                System.out.print(n + " ");
            }
        }
    }}