public class Asteriscos3 {
    public static void main(String[] args) {
        for (int col = 1; col <= 4; col++) { //número de columnas
            System.out.println(); //salto de linea
            for (int cont = 1; cont <= 4; cont++) { //número de repeticiones
                System.out.print("  *");
             }
        }
    }
}
